import streamlit as st
import pandas as pd
import numpy as np
import pickle

# ==========================
# Load Model
# ==========================

model = pickle.load(open("credit_risk_model.pkl", "rb"))

st.set_page_config(
    page_title="Credit Risk Prediction System",
    page_icon="🏦",
    layout="wide"
)

# ==========================
# Title
# ==========================

st.title("🏦 Credit Risk Prediction System")
st.markdown("""
Predict whether a loan applicant is likely to receive loan approval
based on demographic and financial information.
""")

st.divider()

# ==========================
# Input Form
# ==========================

col1, col2 = st.columns(2)

with col1:

    gender = st.selectbox(
        "Gender",
        ["Male", "Female"]
    )

    married = st.selectbox(
        "Married",
        ["Yes", "No"]
    )

    dependents = st.selectbox(
        "Dependents",
        ["0", "1", "2", "3+"]
    )

    education = st.selectbox(
        "Education",
        ["Graduate", "Not Graduate"]
    )

    self_employed = st.selectbox(
        "Self Employed",
        ["Yes", "No"]
    )

with col2:

    applicant_income = st.number_input(
        "Applicant Income",
        min_value=0,
        value=5000
    )

    coapplicant_income = st.number_input(
        "Coapplicant Income",
        min_value=0,
        value=0
    )

    loan_amount = st.number_input(
        "Loan Amount (in thousands)",
        min_value=1,
        value=120
    )

    loan_term = st.number_input(
        "Loan Amount Term",
        min_value=1,
        value=360
    )

    credit_history = st.selectbox(
        "Credit History",
        [1.0, 0.0]
    )

    property_area = st.selectbox(
        "Property Area",
        ["Urban", "Semiurban", "Rural"]
    )

st.divider()

# ==========================
# Feature Engineering
# ==========================

total_income = applicant_income + coapplicant_income

dti = loan_amount / total_income if total_income != 0 else 0

# ==========================
# Encoding
# ==========================

gender = 1 if gender == "Male" else 0

married = 1 if married == "Yes" else 0

education = 0 if education == "Graduate" else 1

self_employed = 1 if self_employed == "Yes" else 0

property_area_mapping = {
    "Rural": 0,
    "Semiurban": 1,
    "Urban": 2
}

property_area = property_area_mapping[property_area]

# Dependents One-Hot Encoding

dep_1 = 0
dep_2 = 0
dep_3 = 0

if dependents == "1":
    dep_1 = 1

elif dependents == "2":
    dep_2 = 1

elif dependents == "3+":
    dep_3 = 1

# Income Group

income_medium = 0
income_high = 0
income_very_high = 0

if total_income > 3000 and total_income <= 6000:
    income_medium = 1

elif total_income > 6000 and total_income <= 10000:
    income_high = 1

elif total_income > 10000:
    income_very_high = 1

# ==========================
# Prediction
# ==========================

if st.button("Predict Risk"):

    input_data = pd.DataFrame({

        "Gender":[gender],
        "Married":[married],
        "Education":[education],
        "Self_Employed":[self_employed],

        "ApplicantIncome":[applicant_income],
        "CoapplicantIncome":[coapplicant_income],
        "LoanAmount":[loan_amount],
        "Loan_Amount_Term":[loan_term],
        "Credit_History":[credit_history],

        "Property_Area":[property_area],

        "TotalIncome":[total_income],
        "DTI":[dti],

        "Dependents_1":[dep_1],
        "Dependents_2":[dep_2],
        "Dependents_3+":[dep_3],

        "Income_Group_Medium":[income_medium],
        "Income_Group_High":[income_high],
        "Income_Group_Very_High":[income_very_high]

    })

    prediction = model.predict(input_data)[0]

    probability = model.predict_proba(input_data)[0][1]

    st.divider()

    st.subheader("Prediction Result")

    if prediction == 1:

        st.success(
            f"✅ Low Risk Borrower\n\nApproval Probability: {probability:.2%}"
        )

    else:

        st.error(
            f"⚠️ High Risk Borrower\n\nApproval Probability: {(1-probability):.2%}"
        )

    st.subheader("Applicant Summary")

    st.write(f"Total Income: ₹ {total_income:,.0f}")
    st.write(f"Loan Amount: ₹ {loan_amount:,.0f}")
    st.write(f"Debt-to-Income Ratio: {dti:.4f}")